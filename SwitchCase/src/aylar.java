package SwitchCase.src;

public class aylar {
  public static void main(String[] args) {
    int ay=12;
    switch (ay) {
        case 1:
            System.out.println("1.ay Ocak ayıdır..");
            break;
        case 2:
            System.out.println("2.ay Şubat ayıdır..");
            break;
        case 3:
            System.out.println("3.ay Mart ayıdır..");
            break;
        case 4:
            System.out.println("4.ay Nisan ayıdır..");
            break;
        case 5:
            System.out.println("5.ay Mayıs ayıdır..");
            break;
        case 6:
            System.out.println("6.ay Haziran ayıdır..");
            break;
        case 7:
            System.out.println("7.ay Temmuz ayıdır..");
            break;
        case 8:
            System.out.println("8.ay Ağustos ayıdır..");
            break;
        case 9:
            System.out.println("9.ay Eylül ayıdır..");
            break;
        case 10:
            System.out.println("10.ay Ekim ayıdır..");
            break;
        case 11:
            System.out.println("11.ay Kasım ayıdır..");
            break;
        case 12:
            System.out.println("12.ay Aralık ayıdır..");
            break;
        default:
            System.out.println("Varsayılan bir ayımız bulunmamaktadır. Lütfen 12 aydan birini giriniz.");
            break;
    }
  }  
}
